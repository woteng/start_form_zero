# MENGAMBIL INPUT DATA DARI USER

# STRING
data = input("masukkan data : ")

print("data :", data, "type :", type(data))

# INTEGER
angkaINT = int(input("masukkan angkaINT : "))

print("angkaINT :", angkaINT, "type :", type(angkaINT))

# FLOAT
angkaFloat = float(input("masukkan angkaFloat : "))

print("angkaFloat :", angkaFloat, "type :", type(angkaFloat))

# BOOOLEAN BINER
# di pindahkan dulu ke integer
angkaBiner = bool(int(input("masukan angkaBiner :")))

print("angkaBiner :", angkaBiner, "type :", type(angkaBiner))
